/**
 * 生成模拟的实时日志条目（用于训练监控页的日志流追加）
 */
export function generateMockLog(epoch: number, step: number): ITrainingLog {
  const messages = [
    `Step ${step}: 前向传播完成，batch loss: ${(0.08 + Math.random() * 0.05).toFixed(4)}`,
    `Step ${step}: 反向传播完成，梯度范数: ${(0.5 + Math.random() * 1.5).toFixed(4)}`,
    `Step ${step}: 参数更新完成，学习率: ${(0.001 * Math.pow(0.97, epoch)).toFixed(6)}`,
    `GPU 显存使用: ${(32 + Math.random() * 8).toFixed(1)}GB / 80GB`,
    `数据加载: 批次 ${step % 2002} / 2002, 耗时 ${(0.02 + Math.random() * 0.05).toFixed(3)}s`,
    `Step ${step}: 当前 batch accuracy: ${(90 + Math.random() * 9).toFixed(2)}%`,
  ];

  const levels: Array<ITrainingLog['level']> = ['info', 'info', 'info', 'info', 'debug', 'info'];
  const idx = Math.floor(Math.random() * messages.length);

  return {
    id: `log-realtime-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`,
    timestamp: new Date().toISOString(),
    level: levels[idx],
    message: messages[idx],
    epoch,
    step,
  };
}
